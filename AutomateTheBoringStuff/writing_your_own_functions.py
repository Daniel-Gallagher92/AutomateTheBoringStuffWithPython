#new example to include argument within hello function
def hello(name):
  print('Well Hello! ' + name)
  print('Hiya! ' + name)
  print('HowDEEEE!!!! ' + name)
  print('\n')

hello('Brittani')
hello('Daniel')
hello('Valerie')